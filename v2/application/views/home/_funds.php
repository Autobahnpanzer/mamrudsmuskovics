      <h2 class="title">Fonds</h2>

      <p class="txt intro">Die große Anzahl von offenen und geschlossenen Immobilienfonds in Deutschland und der nennenswerte Immobilienbestand, der sich in deren Besitz befindet, hat uns bereits vor einiger Zeit dazu veranlasst, uns intensiv mit der »Fondswelt« zu beschäftigen.</p>

      <p class="txt">Seit 2011 haben wir uns auf die Beteiligung an – und die Übernahme von – geschlossenen Immo&shy;bi&shy;lienfonds spezialisiert. Erfahrungsgemäß sind unsere Kaufangebote für viele Gesellschafter eine willkommene Alternative zum langfristigen Verbleib in der Gesellschaft. Bis heute haben über 3000 Anleger diese Möglichkeit genutzt und ihre Beteiligungen an uns veräußert.</p>

      <p class="txt">Zwischenzeitlich halten wir die Mehrheit einer Reihe großer Publikumsfonds mit Wohnbeständen in der Rechtsform der GbR, eines Publikumsfonds mit einer Hotelimmobilie in der Rechtsform der GmbH & Co. KG und weitere Beteiligungen verschiedener Fondsgesellschaften.</p>

            <p class="txt">Unsere Arbeit in diesem Bereich wird neben führenden Fondsgeschäftsführungsgesellschaften insbe&shy;sondere auch von Sanierungsberatern, Anlegerschutzverbänden, Wirtschaftsprüfern, Anlage&shy;beratern und Banken begleitet.</p>

      <p class="txt">Für Rückfragen zu diesem Geschäftsfeld stehen wir Ihnen gerne zur Verfügung.</p>
